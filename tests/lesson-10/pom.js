/* POM = Page Object Model
- là 1 desgin pattern (mấu thiết kế): tổ chức code gọn gàng hơn
- thường sử dụng trong automation test: tạo ra những lớp đối tượng đại diện cho các page
- Lợi ích của POM:
    - Tái sử dụng code
    - Giúp tổ chức code gọn gàng
    - Dễ bảo trì
- Note: Tiêu chuẩn của POM
    Lưu ý: không có 1 chuẩn chung nào cho POM
        Dựa trên:
        - Framework
        - Ngôn ngữ
        - Author
        - Sở thích
- Cách viết POM: 
    Core concept:
    - Mỗi page là một class
    - Có thuộc tính (property) và phương thức (method) riêng
        + Property (thuộc tính): biểu thị các phần tử trên trang
        + Method (phương thức): biểu thị hành động của người dùng

- Multiple POM: extends
    Core concept:
    + Mỗi page là một class
    + Có thuộc tính và phương thức riêng

- Note: override property


*/